# Вхідні дані
a = int(input())  # довжина прямокутника
b = int(input())  # ширина прямокутника
c = int(input())  # сторона квадрата

# Обчислення кількості квадратів
squares_in_a = a // c  # скільки квадратів вміщується по довжині
squares_in_b = b // c  # скільки квадратів вміщується по ширині

total_squares = squares_in_a * squares_in_b  # загальна кількість квадратів

# Вивід результату
print(total_squares)